    private void HaeKuvatJaVihjeet() {
        kuvat = varasto.haeKaikkiKuvat();
        for (Kuva k : kuvat) {
            k.setVihjeet(varasto.haeVihjeetKuvalle(k));
        }

        randomaaKuva();
    }

    private void randomaaKuva() {
        valittuKuva = kuvat.get(rand.nextInt(kuvat.toArray().length));
        ImageIcon kuva = new ImageIcon("kuvat\\" + valittuKuva.getPolku());
        ImageIcon asd = new ImageIcon(kuva.getImage().getScaledInstance(250, 300, 1));
        kivaKuva.setText(null);
        kivaKuva.setIcon(asd);
        repaint();
    }

